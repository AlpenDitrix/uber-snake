package com.avapir.snake.Interfaces;

/**
 * Objects of class which implements {@code Relocatable} must be able to change
 * his location in some representation. Of course objects of that class will
 * <i>not must</i> to move every time, but <i>must</i>
 * 
 * @author Alpen Ditrix
 * 
 */
public interface Relocatable {

	/**
	 * Method describes the movement of the object.
	 */
	public void move();

}
